'''
Exercise: Math Constants

This script prints all the mathematical constants from the math module.

Usage:
    Call the main function.

Tags: math module, constants
'''

import math


def main():
    '''Print all mathematical constants.'''
    print('Math constants:')
    print(f'pi: {math.pi}')
    print(f'e: {math.e}')
    print(f'tau: {math.tau}')
    print(f'inf: {math.inf}')
    print(f'nan: {math.nan}')


if __name__ == '__main__':
    main()
