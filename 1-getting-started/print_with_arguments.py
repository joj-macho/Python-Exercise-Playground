'''
Exercise: Print with Arguments

This script demonstrates the use of print() with arguments sep and end.

Usage:
    Call the main function to see print() with arguments in action.

Tags: print arguments, sep, end
'''


def main():
    '''Print using sep and end arguments.'''
    print('Hello', 'World', sep='-', end='!!!\n')
    print('Python', 'Programming', sep=' ', end='.\n')


if __name__ == '__main__':
    main()
