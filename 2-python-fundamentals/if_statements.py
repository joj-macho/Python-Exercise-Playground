'''
Exercise: If Statements

This script demonstrates how to use if statements to make decisions.

Usage:
    Call the main function to see various examples of if statements in action.

Tags: if-elif-else, decision making
'''


def main():
    '''Demonstrates the use of if statements.'''
    x = 4
    if x > 2:
        print('x is greater than 5')
    else:
        print('x is not greater than 5')

    y = 'python'
    if y == 'Python':
        print('y is Python')
    else:
        print('y is not Python')


if __name__ == '__main__':
    main()
