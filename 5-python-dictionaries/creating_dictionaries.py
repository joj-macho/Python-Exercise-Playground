'''
Exercise: Creating Dictionaries

This script demonstrates how to create and initialize dictionaries.

Usage:
    Call the main function to see various examples of creating dictionaries.

Tags: dictionaries, initialization, basics
'''


def main():
    '''Demonstrates creating dictionaries.'''
    my_dict = {'name': 'Alice', 'age': 20, 'pet': 'cat'}
    print(f'Dictionary: {my_dict}')

    empty_dict = {}
    print(f'Empty dictionary: {empty_dict}')


if __name__ == '__main__':
    main()
